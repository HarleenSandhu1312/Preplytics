export default function Sidebar({ page, onNav }) {
  const { theme, toggle } = useTheme();
  const { user, logout }  = useAuth();
  const items = user?.role === 'admin' ? ADMIN_ITEMS : STUDENT_ITEMS;

  return (
    <aside className="sidebar">
      {/* Brand — PrepLytics text, color changes with theme */}
      <div className="sb-brand">
        <span className="sb-wordmark">Prep<span>Lytics</span></span>
      </div>

      {/* Nav — SVG icon centered on top, label below */}
      <nav className="sb-nav">
        {items.map(it => (
          <button
            key={it.id}
            className={`sb-item ${page === it.id ? 'active' : ''}`}
            onClick={() => onNav(it.id)}
            title={it.label}
          >
            <span className="sb-icon">{Icons[it.icon]}</span>
            <span className="sb-label">{it.label}</span>
          </button>
        ))}
      </nav>

      <div className="sb-bottom">
        <button className="sb-item" onClick={logout} title="Sign Out">
          <span className="sb-icon">{Icons.signout}</span>
          <span className="sb-label">Sign Out</span>
        </button>
        <button className="sb-item sb-theme" onClick={toggle} title="Toggle theme">
          <span className="sb-icon">{theme === 'dark' ? Icons.sun : Icons.moon}</span>
          <span className="sb-label">{theme === 'dark' ? 'Light' : 'Dark'}</span>
        </button>
      </div>
    </aside>
  );
}
